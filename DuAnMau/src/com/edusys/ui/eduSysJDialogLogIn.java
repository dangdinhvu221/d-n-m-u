    public void dangNhap() {
        String maNV = txtTenDangNhap.getText();
        String matKhau = new String(txtMatKhau.getPassword());
        if (Validate.checkEmty(this, txtTenDangNhap, "Vui lòng điền tài khoản!!!", "Error!!!") == false) {
            return;
        } else if (Validate.checkEmty(this, txtMatKhau, "Vui lòng điền mật khẩu!!!", "Error!!!") == false) {
            return;
        } else {
            NhanVien nhanVien = dao.selectByID(maNV);
            if (nhanVien == null) {
                mesageDiaLogHelper.showErrorDialog(this, "Sai tên đăng nhập!!!", "Error");
                txtTenDangNhap.setBorder(new LineBorder(Color.RED));
            } else if (!matKhau.equals(nhanVien.getMatKhau())) {
                mesageDiaLogHelper.showErrorDialog(this, "Sai mật khẩu!!!", "Error");
                txtMatKhau.setBorder(new LineBorder(Color.RED));
            } else {
                txtTenDangNhap.setBorder(new LineBorder(Color.GREEN));
                txtMatKhau.setBorder(new LineBorder(Color.GREEN));
                Auth.user = nhanVien;
                mesageDiaLogHelper.showMessageDialog(this, "Đăng nhập thành công!", "Thông báo!!!");
                this.dispose();
//            new EduSysJFrameHTQLDT().setVisible(true);
            }
        }

    }

    public void ketThuc() {
        int chosse = mesageDiaLogHelper.showComfirmDialog(this, "Bạn có muốn kết thúc ứng dụng?", "Thông báo!!!");
        if (chosse == JOptionPane.YES_OPTION) {
            System.exit(0);
        }
    }

